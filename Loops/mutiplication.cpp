/*Print multiplication table of 24, 50 and 29 using loop.*/


#include <iostream>
using namespace std;

int main() {
  for(int i=1;i<=10;i++)
  {

    cout<<"24x"<<i<<"="<<24*i<<endl;

  }
  cout<<endl;

  for(int i=1;i<=10;i++)
  {

    cout<<"50x"<<i<<"="<<50*i<<endl;

  }
  cout<<endl;

  for(int i=1;i<=10;i++)
  {

    cout<<"29x"<<i<<"="<<29*i<<endl;

  }
  return 0;
}
